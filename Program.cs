        toProcessPoints.Push(points[START_X,START_Y]);
        points[START_X, START_Y].visited = true;
        bitmap.SetPixel(points[START_X,START_Y].x,points[START_X,START_Y].y, Color.FromArgb(1,rnd.Next(256),rnd.Next(256),rnd.Next(256)));

        var checkpoints = Enumerable.Range(1, 10).ToDictionary(i => i * WIDTH*HEIGHT / 10 - 1, i => i - 1);

        for (int i = 0; i < WIDTH * HEIGHT; i++)
        {
            Point p = toProcessPoints.Pop();

            Point[] neighborsPos = GetNeighbors(p,rnd.Next(1012));

            for(int j = 0; j < 8; j++)
            {
                Point pos = neighborsPos[j];
                if(IsInBound(neighborsPos[j]) && !points[pos.x,pos.y].visited)
                {
                    bitmap.SetPixel(points[pos.x,pos.y].x, points[pos.x,pos.y].y, GetColor(bitmap.GetPixel(p.x, p.y), rnd));//
                    toProcessPoints.Push(points[pos.x,pos.y]);
                    points[pos.x,pos.y].visited = true;
                }
            }

            // save a checkpoint
            int chkidx;
            if (checkpoints.TryGetValue(i, out chkidx))
            {
                bitmap.Save("Result"+ chkidx + ".jpg", System.Drawing.Imaging.ImageFormat.Jpeg);
                Console.WriteLine("To Process : " + toProcessPoints.Count());
                Console.WriteLine("Advancement : " + (toProcessPoints.Count()/(WIDTH * HEIGHT)));
            }
        }                            
    }
