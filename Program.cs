    struct Point
    {
        public int x,y;
        public bool visited;
        public Point(int x ,int y)
        {
            this.x = x;
            this.y = y;
            visited = false;
        }
    }

    static List<Color> GetColorsNextTo(Color c)
    {
        List<Color> neighbors = new List<Color>();
        if(IsValidRGBValue(c.R + COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R + COLOR_OFFSET, c.G, c.B));        
        if(IsValidRGBValue(c.R - COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R - COLOR_OFFSET, c.G, c.B));
        if(IsValidRGBValue(c.G + COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R, c.G + COLOR_OFFSET, c.B));        
        if(IsValidRGBValue(c.G - COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R, c.G - COLOR_OFFSET, c.B));
        if(IsValidRGBValue(c.B + COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R, c.G, c.B + COLOR_OFFSET));        
        if(IsValidRGBValue(c.B - COLOR_OFFSET))
            neighbors.Add(Color.FromArgb(1, c.R, c.G, c.B - COLOR_OFFSET));

        return neighbors;
    }

    static bool IsValidRGBValue(int value) => value >= 0 && value < 256;

    static Color GetColor(Color c, Random rnd)
    {
        var neighbors = GetColorsNextTo(c);
        int i = rnd.Next(neighbors.Count());
        return neighbors[i];
    }

    static Point[] GetNeighbors(Point p, int rnd)
    {
        Point[] neighbors = {
            new Point(p.x - 1, p.y - 1),
            new Point(p.x - 1, p.y),
            new Point(p.x - 1, p.y + 1),
            new Point(p.x + 1, p.y - 1),
            new Point(p.x + 1, p.y),
            new Point(p.x + 1, p.y + 1),
            new Point(p.x, p.y - 1),
            new Point(p.x, p.y + 1)
        };

        Point[] rndNeighbors = new Point[8];

        for (int i = 0; i < 8; i++)
        {
            rndNeighbors[i] = neighbors[perms[rnd,i]];
        }

        return rndNeighbors;
    }

    static bool IsInBound(Point p)
    {
        return p.x >= 0 && p.x < WIDTH && p.y >= 0 && p.y < HEIGHT;
    }

